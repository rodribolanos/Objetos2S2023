package ar.edu.unq.po2.tp1.empresa;

public class Concepto {
	private String nombre;
	private int valor;
	
	public Concepto(String nombre, int valor) {
		this.nombre = nombre;
		this.valor = valor;
	}

	public String getNombre() {
		return nombre;
	}

	public int getValor() {
		return valor;
	}
	
	
}
